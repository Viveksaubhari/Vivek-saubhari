public class Relational_Comparison_Operators {
    public static void main(String[] args) {
        int a = 7, b = 11;
        System.out.println(a == b); // false
        System.out.println(a != b); // true
        System.out.println(a > b);  // false
        System.out.println(a < b);  // true
    }
}
